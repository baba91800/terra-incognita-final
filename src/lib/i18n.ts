export const T: Record<Lang, Translations> = { fr, en, es, de, pt }

const LANG_KEY = 'ti2_lang'
export function loadLang(): Lang {
  const saved = localStorage.getItem(LANG_KEY) as Lang
  if (saved && T[saved]) return saved
  const browser = navigator.language.slice(0, 2) as Lang
  return T[browser] ? browser : 'fr'
}
export function saveLang(lang: Lang) { localStorage.setItem(LANG_KEY, lang) }
export function useT(lang: Lang): Translations { return T[lang] }
